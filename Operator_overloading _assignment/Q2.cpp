#include<iostream>
using namespace std;
class sample
   {
	private:
		char ch;
	public:
		sample(char ch)
		{
			this->ch=ch;
		}
		void display();
  };

void sample ::display()
{
	cout<<ch<<endl;
}

int main()
{
	sample a(65);
	a.display();
	return 0;
}